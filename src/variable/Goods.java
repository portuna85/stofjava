package variable;

public class Goods {
    public static void main(String[] args) {
        int price = 100;            // 가격 변수 선언과 동시에 초기화
        int goodCount;              // 상품 갯수 변수 선언만 한다.
        goodCount = 3;              // 상품 갯수 변수 초기화만 한다.

        System.out.println(price);
        System.out.println(goodCount);
    }
}
