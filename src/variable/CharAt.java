package variable;

public class CharAt {
    public static void main(String[] args) {
        String str = "14";

        System.out.println(str.charAt(1));
        System.out.println('A' + 'A');
        System.out.println(Integer.parseInt(str));
        System.out.println(str + 1);
        System.out.println(3 + '0');
    }
}