package Oop;

public class Car {
    public String color;
    public String name;
    public String gearType;
    public int door;

    public Car() {
    }

    public Car(String color, String name, String gearType, int door) {
        this.color = color;
        this.name = name;
        this.gearType = gearType;
        this.door = door;
    }

}