package Operator;

public class OperatorEx5 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println(a + b);
        System.out.println((float) a + b);
        System.out.println(a - (float) b);
        System.out.println(a / (float) b);
    }
}